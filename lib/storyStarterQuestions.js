/**
 * Pick 3 random non-repeating categories and one question from each.
 * @param {string} lang - 'KO' | 'EN' | 'ES'
 * @returns {Array<{ cat, emoji, question }>}
 */
export function pickStarterCards(lang = 'KO') {
  const cats = STORY_QUESTIONS[lang] || STORY_QUESTIONS.KO;
  const shuffled = [...cats].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, 3).map(cat => ({
    cat     : cat.cat,   // string, e.g. '어린 시절'
    emoji   : cat.emoji,
    question: cat.questions[Math.floor(Math.random() * cat.questions.length)],
  }));
}
