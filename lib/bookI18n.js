/**
 * Hook-friendly accessor. Reads localStorage on mount via getUserLang
 * and returns the matching message bundle. Falls back to ko.
 */
export function useBookMsgs() {
  const lang = getUserLang();
  return BOOK_MSGS[lang] || BOOK_MSGS.ko;
}

/** Substitute {key} placeholders ("Delete {kind}" + {kind:'chapter'}). */
export function fmt(template, vars) {
  if (!template) return '';
  if (!vars) return template;
  return template.replace(/\{(\w+)\}/g, (_, k) => (vars[k] != null ? String(vars[k]) : ''));
}
